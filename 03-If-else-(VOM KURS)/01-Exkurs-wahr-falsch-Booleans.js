"use strict"

// Exkurs Booleans: Variablen, die true oder false enthalten

// let x = 5 + 6

let greeting = "Hallo Welt"

let a = (greeting + "!") === "Hallo Welt!"
let b = (greeting + "!") !== "Hallo Welt!"

console.log("a: ", a) //true
console.log("b: ", b) //false

// Mathematische Vergleiche
let zahl1 = 5
let zahl2 = 6

console.log("zahl1 === zahl2:",  zahl1 === zahl2) //false
console.log("zahl1 !== zahl2:",  zahl1 !== zahl2)//true
console.log("zahl1 < zahl2:",  zahl1 < zahl2)//true
console.log("zahl1 <= zahl2:", zahl1 <= zahl2)//true
console.log("zahl1 > zahl2:",  zahl1 > zahl2) //false
console.log("zahl1 >= zahl2:", zahl1 >= zahl2) //false

